    {
        std::cout << " generation failed.\n\n";
        return -1;
    }

    air_cpp_file << license;
    air_cpp_file << "// generated by c_interface_gen module\n\n";
    air_cpp_file << "#include \"src/api/Air_c.h\"\n";
    air_cpp_file << "#include \"src/api/Air.h\"\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIR_INITIALIZE(uint32_t cpu_num) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), true>::Initialize(cpu_num);\n";
    air_cpp_file << "}\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIR_ACTIVATE(void) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), true>::Activate();\n";
    air_cpp_file << "}\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIR_DEACTIVATE(void) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), true>::Deactivate();\n";
    air_cpp_file << "}\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIR_FINALIZE(void) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), true>::Finalize();\n";
    air_cpp_file << "}\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIRLOG(uint32_t node_id, uint32_t filter_item, uint64_t node_index, uint64_t value) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), true>::\n";
    air_cpp_file << "    LogData(node_id, filter_item, node_index, value);\n";
    air_cpp_file << "}\n\n";

    air_cpp_file << "extern \"C\"\n";
    air_cpp_file << "void AIRLOG_FAKE(uint32_t node_id, uint32_t filter_item, uint64_t node_index, uint64_t value) {\n";
    air_cpp_file << "    AIR<cfg::GetIntValue(config::ParagraphType::DEFAULT, \"AirBuild\"), false>::\n";
    air_cpp_file << "    LogData(node_id, filter_item, node_index, value);\n";
    air_cpp_file << "}\n";

    air_cpp_file.close();
    std::cout << " - Air_c.cpp\n";

    // Air_c.cpp gen end

